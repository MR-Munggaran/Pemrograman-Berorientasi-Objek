package Nomor4;

class Lingkaran extends Utama{
    double hasilluas;
    public double luaslingkaran(double jariku, double phi){
        hasilluas = phi*jariku*jariku;
        return hasilluas;
    }
    public double kelilingkaran(double jariku, double phi){
        hasilling = 2 * jariku * phi;
        return hasilling;
    }
}
